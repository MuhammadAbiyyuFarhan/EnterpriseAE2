     <p align="justify">Cloud Computing dalam bahasa Indonesia diterjemahkan menjadi Komputasi Awan.
        ‘Komputasi’ diterjemahkan sebagai gabungan pemanfaatan teknologi komputer, sedangkan
        ‘Awan’ merupakan metafora dari pengembangan infrastruktur berbasis internet.
        Secara harfiyah, definisi Cloud Computing diartikan sebagai kumpulan seperangkat teknologi
        yang biasanya membentuk lingkungan cloud. Namun Rountree, Derrick; Castrillo, 2014 menilai
        kumpulan teknologi tersebut bukan inti dari Cloud Computing.
        Cloud Computing merupakan gaya komputasi baru di mana sumber daya yang terukur secara
        dinamis dan sering kali menggunakan konsep virtualisasi yang disediakan oleh provider dengan
        memanfaatkan media internet.</p> 
        
    <h2 align="center">SEJARAH KEHADIRAN TEKNOLOGI CLOUD</h2>
    <br>

    <p align="justify">Sejarah komputasi awan berawal dari konsep yang muncul pada tahun 1950 dengan ide yang
      implementasikan komputere mainframe yang dapat diakses melalui klien statis, sejak saat
      itu komputasi awan mulai mengalami evousi dari klien statis berubah menjadi klien dinamis
      dan dari perangkat lunak ke layanan. Sedangkan pengertian cloud computing secara luas,
      dengan kata lain kita bias menyatakan bahwa cloud adalah sesusatu yang ada dan hadir tadi
      dengan jarak yang jauh. Contoh penggunaan cloud bias kita lihat seperti Email, Github,
      Office 365 dijalankan dengan menggunakan system cloud. Cloud juga menyediakan
      layanannya melalui jaringan secara public maupun secara pribadi misalnya WAN, LAN,
      maupun VPN <p>
    <p align="justify">Berdasarkan sejarahnya, Cloud Computing merupakan hasil evaluasi bertahap. Konsep
      penggabungan computing resources melalui jaringan global disebut-sebut berawal pada
      tahun 1960-an seiring dengan kemunculan ‘Intergalactic Computer Network’ oleh JCR
      Licklider.<p>
    
    <p align="justify">Ia juga bertanggung jawab atas pembangunan Advanced Research Projects Agency Network
      (ARPANET) tahun 1969. Saat itu ia memiliki cita-cita bahwa seluruh manusia di dunia
      seharusnya bisa terhubung dan mengakses program dan data dari sarana manapun dan di
      manapun.<p>
  
    <p align="justify">Namun pandangan lain diungkapkan Herwin Anggeriana dalam bukunya ‘Cloud
      Computing: Komputasi Awan’. Menurut dia, hal yang mendasari konsep Cloud Computing
      berawal pada 1960-an.<p>
  
    <p align="justify">Ia juga bertanggung jawab atas pembangunan Advanced Research Projects Agency Network
          (ARPANET) tahun 1969. Saat itu ia memiliki cita-cita bahwa seluruh manusia di dunia
          seharusnya bisa terhubung dan mengakses program dan data dari sarana manapun dan di
          manapun.<p>
  
    <p align="justify">Namun pandangan lain diungkapkan Herwin Anggeriana dalam bukunya ‘Cloud
          Computing: Komputasi Awan’. Menurut dia, hal yang mendasari konsep Cloud Computing
          berawal pada 1960-an.<p>

    <p align="justify">Saat itu, John McCarthy, Pakar Komputasi dari Massachusetts Institute of Technology
    (MIT) yang juga dikenal sebagai salah satu pionir kecerdasan buatan menyampaikan
    gagasan bahwa “suatu hari nanti jaringan komputasi akan menjadi infrastruktur publik, sama
    halnya seperti listrik dan telepon.” <p>
      
              
    <p align="justify">Dalam perkembangannya, Cloud Computing sebenarnya tumbuh bersama internet dan web.
    Terlihat bahwa pendorong utama teknologi komputasi ialah adanya revolusi internet.
              Pada era 1990-an, perusahaan telekomunikasi mulai menawarkan VPN layanan jaringan
              pribadi dengan kualitas lebih baik, tapi biaya lebih rendah. Untuk menyeimbangkan
              penggunaan server, mereka menggunakan bandwidth jaringan, lalu menggunakan simbol
              awan sebagai petunjuk titik demarkasi antara provider dan pengguna. Pada dasarnya,
              Komputasi Awan memperluas batas untuk menutup server serta infrastruktur jaringan.
              Sampai akhirnya pada 1995, Larry Ellison, Pendiri Oracle memunculkan ide Network
              Computing sebagai kampanye untuk menggugat dominasi Microsoft yang saat itu merajai
              Desktop Computing dengan Windows 95.<p>
      
    <p align="justify">Ellison menawarkan ide bahwa pengguna tidak membutuhkan software sistem operasi yang
                dijejalkan ke dalam PC mereka, melainkan dengan Network Computing sebagai pengganti
                desktop. Namun akhirnya inovasi itu lenyap karena kualitas jaringan komputer yang belum
                memadai, dan pengguna kembali memilih PC desktop.</p>
      
    <p align="justify">Seiring perkembangan performa jaringan komputer pada akhir 1990-an, lahir konsep
                penerus Network Computing yaitu Application Service Provider (ASP) yang menawarkan
                akses program aplikasi tertentu melalui web. Faktor pendorong kemajuan konsep ini adalah
                munculnya berbagai layanan hosting dan bahasa pemrograman berbasis web.
                Sampai akhirnya muncul terobosan baru bernama Cloud Computing yang tak bergantung
                pada sistem operasi tertentu, melainkan hanya perlu mengakses internet dan menuju alamat
                situs untuk menjalankan program yang dibutuhkan.</p>
      
    <p align="justify">Popularitas Komputasi Awan semakin menjulang saat munculnya layanan dalam bentuk
                software as a service (SaaS) Salesforce.com pada tahun 1999. Aplikasi Customer
                Relationship Management (CRM) ini dianggap sebagai pencetus pertama aplikasi
                perusahaan yang dijalankan melalui Internet.</p>
      
    <p align="justify">Perkembangan berikutnya pada 2005 Amazon Web Services meluncurkan teknologi Elastic
                Compute Cloud (EC2), Google App Engine, dan IBM Blue Cloud Initiative. Jadi, terdapat
                situs layanan web yang di komersialkan dan memungkinkan individu atau perusahaan kecil
                untuk menyewa server agar dapat menjalankan aplikasi komputer mereka.
                di Indonesia, terdapat pula pemain-pemain besar yang menjalankan bisnis berbasis Cloud
                Computing, seperti perusahaan milik pemerintah PT Telekomunikasi Indonesia (Telkom)
                Tbk. Telkom menyediakan layanan Cloud Computing untuk memenuhi kebutuhan
                pengguna.</p>
              
    <h2 align="center">KONSEP DASAR</h2>
    <br>

    <p align="justify">Komputasi awan merupakan sebuah sistem yang mengacu pada kegiatan manipulasi,
      konfigurasim dan juga akses data dari hardware dan juga software dari jarak jauh
      menggunakan jaringan internet, dengan menyediakan penyimpanan data berbasis online, infrastruktur, maupun dari sebuah aplikasi. Sistem komputasi awan memberikan kita sebuah
      penawaran yang di namakan independensi platform yang merupakan sebuah model sistem
      dari software yang tidak bergantung pada platform teknologi spesifik atau sering digunakan
      dalam konteks pendekatan pada arsitektur berbasis model. Kelebihannya kita tidak perlu
      menginstal software di lokal PC yang menjadikan komputasi awan sebagai aplikasi bisnis
      mobile dan juga kolaboratif</p>

    <h2 align="center">ARSITEKTUR</h2>
    <br>
    <p align="justify"> 
    Komputasi awan memiliki sebuah arsitekture yang terdiri dari Front end dan Back end yang
    menjadi sebuah hal penting dalam pembentukan dari system komputasi awan.</p>

     <p align="justify" style="text-indent: 20px;">- Front end</p>
     
       <p align="justify" style="text-indent: 30px;">Front end merupakan sebuah proses yang meliputi dari infrastructure yang digunakan oleh
       para penggunanya.</p> 
       
     <p align="justify" style="text-indent: 20px;">- Back end</p>

      <p align="justify" style="text-indent: 30px;"> Back end merupakan sebuah proses yang meliputi dari segi aplikasi, management, service,
       cloud runtime, penyimpanan,</p> <p align="justify" style="text-indent: 30px;">keamanan, dan juga infrastruktur pusat. Fungsi back end lebih
       memegang peranan karena back end memiliki tugas untuk</p> <p align="justify" style="text-indent: 30px;">mengurusi segala urusan yang
       tidak perlu pengguna atau kllien urusi.
      </p> 

