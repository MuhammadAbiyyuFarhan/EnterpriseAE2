      <p align="justify">Model sistem penerapan merupakan model yang mengunakan sistem penyebaran yang
                                          menentukan jenis dari akses cloud mulai dari bagamana cloud tersebut berada pada jenis
                                          tertentu, cloud dapat memilih tempat atau akses dari 4 jenis akses yang tersedia yaitu:</p>
      <br>
      <h2 align="center">Public Cloud</h2>
                            
      <p align="justify">Public cloud merupakan akses cloud yang bisa dengan mudah diakses oleh orang-
                                            orang umum tapi memiliki tingkat keamanan yang sangat kurang karena sistem
                                           public cloud bersifat umum jadi mudah untuk di bajak oleh orang lain.</p>
    
      <p align="justify">Public cloud memiliki system dan layanan yang mudah di akses oleh banyak
                                              masyarakat umum. Bisa kita ambil contoh dengan para raksasa IT seperti Google,
                                              Amazon, dan Microsoft memberikan penawaran sebuah layanan cloud melalui
                                              internet sebagai penyimanan data-data para pengguna.</p>
    
      <p align="justify">Dalam penggunaan system public cloud seluruh data-data yang diinput dari para
                                                pengguna akan di tampung oleh administrator dan dikordinasikan untuk dimasukan ke data pusat secara rapih dan juga tersusun. Dalam public cloud
                                                memiliki beberapa manfaat dan juga kelemahan sebagai berikut:</p>
    
      <p align="justify" style="text-indent: 20px;">a. Manfaat</p>
      <p align="justify" style="text-indent: 40px;">• Hemat biaya</p>
      <p align="justify" style="text-indent: 40px;">• Keandalan</p>
      <p align="justify" style="text-indent: 40px;">• Fleksibilitas</p>
      <p align="justify" style="text-indent: 40px;">• Lokasi kemerdekaan</p>
      <p align="justify" style="text-indent: 40px;">• Biaua gaya utilitas</p>
      <p align="justify" style="text-indent: 40px;">• Skalabilitas tinggi</p>

      <p align="justify" style="text-indent: 20px;">b. Kelemahan</p>
      <p align="justify" style="text-indent: 40px;">• Keamanan rendah</p>
      <p align="justify" style="text-indent: 40px;">• Kurang dapat disesuaikan</p>  

      <br>                                       
    
      <h2 align="center">Private Cloud</h2>
      <p align="justify">Private cloud merupakan akses cloud yang memiliki kemungkinan bahwa sistem
        dan layanannya hanya bisa di akses oleh penggunanya sendiri maupun sebuah
        organisasi yang memiliki akses tersebut, dan akses private cloud memiliki tingkat
        keamanan lebih tinggi dikarenakan aksesnya bersifat privasi.</p>

        <p align="justify" style="text-indent: 20px;">a. Manfaat</p>

        <p align="justify" style="text-indent: 40px;">• Keamanan dan privasi yang tinggi</p>
        <p align="justify" style="text-indent: 40px;">• Kendali lebih</p>
        <p align="justify" style="text-indent: 40px;">• Efisiensi daari biaya dan juga energi</p>

        <p align="justify" style="text-indent: 20px;">b. Kelemahan</p>
        <p align="justify" style="text-indent: 40px;">• Lingkup terbatas</p>
        <p align="justify" style="text-indent: 40px;">• Harga mahal</p>
        <p align="justify" style="text-indent: 40px;">• Skalabilitas terbatas</p>
        <p align="justify" style="text-indent: 40px;">• Kemampuan tambahan</p> 

        <br>

        <h2 align="center">Hybrid Cloud</h2>

        <p align="justify">Hybrid cloud merupakan campuran dari dua jenis akses cloud yaitu Public cloud
          dan juga Private cloud, adanya penggabungan dua jenis akses ini bertujuan untuk
          memilah data yang penting dan jua kurang penting yang bisa di simpan pada akses
          yang berbeda, untuk data penting di gunakan akses secara privasi dan yang kurang
          penting bisa menggunakan akses secara public.</p>                                          
       <center><img src="assets/img/hybrid.JPG" alt="Cloud Computing" style="height: 300px;"></center>
                                                     
       <p align="justify">Hybrid cloud memiliki system yang unik yaitu dengan menggabungkan dua jenis
        system dari system privat cloud dan public cloud yang memiliki fungsi untuk
        
        membagikan atau memilah data yang termasuk dalam data kritis maupun non-
        kritis. Manfaat dan kelemahan dalam hybrid cloud bisa di lihat sebagai berikut:</p>
                                               
        <p align="justify" style="text-indent: 20px;">a. Manfaat</p>
   
        <p align="justify" style="text-indent: 40px;">1. Efektivitas biaya</p>

        <p align="justify" style="text-indent: 60px;">Seperti yang diketahui, hybrid cloud platform memberikan biaya lebih
          murah dari platform cloud yang lain. Anda</p> <p align="justify" style="text-indent: 60px;">dapat dengan mudah
          menggunakan public cloud dan dapat sewaktu – waktu menggunakan
          private cloud dengan</p> <p align="justify" style="text-indent: 60px;"> biaya lebih hemat.</p>

        <p align="justify" style="text-indent: 40px;">2. Keamanan</p>
          
      <p align="justify" style="text-indent: 60px;">Tak perlu khawatir karena semua data sensitif perusahaan tetap terjaga di
            private cloud.</p>

        <p align="justify" style="text-indent: 40px;">3. Penyimpanan</p>                                                 
   
       <p align="justify" style="text-indent: 60px;">Hybrid cloud memungkinkan perusahaan memiliki penyimpanan yang
                                                             lebih efisien dan besar karena adanya integrasi</p> <p align="justify" style="text-indent: 60px;"> private cloud dan public
                                                             cloud.</p>
   
       <p align="justify" style="text-indent: 40px;">4. Keandalan</p>
   
      <p align="justify" style="text-indent: 60px;">Saat melakukan pendistribusian data baik dari private cloud ke public cloud
                                                               atau sebaliknya, hybrid cloud tetap bisa</p> <p align="justify" style="text-indent: 60px;"> menghasilkan performa yang
                                                               optimal.</p>
   
        <p align="justify" style="text-indent: 60px;">Dari segi keuntungan sangat banyak sekali, mulai dari data yang aman,
                                                                 efisiensi biaya dan bisa leluasa memilih bisnis</p> <p align="justify" style="text-indent: 60px;"> mana yang berjalan di Public
                                                                 Cloud ataupun Private Cloud dengan tetap menjamin integrasi dari kedua
                                                                 layanan</p> <p align="justify" style="text-indent: 60px;"> tersebut.</p>
        <br>
   
        <p align="left">Community Cloud</p>
   
        <p align="justify">Community Cloud merupakan infrastruktur cloud yang digunakan secara bersama
                                                                   oleh beberapa organisasi yang memiliki kepentingan yang sama, dan kebutuhan
                                                                   tingkat keamanan yang sama.</p>
   
         <p align="justify">Komputasi jenis ini digunakan secara eksklusif didasari oleh poin-poin
                                                                     pertimbangan kesamaan. Misalnya saja kebutuhan keamanan yang sama, misi dan
                                                                     kebijakan yang sama, atau pertimbangan kepatuhan yang serupa.</p>
   
        <p align="justify">Cloud kemungkinan dimiliki dan dikelola oleh satu atau lebih organisasi dalam
                                                                       komunitas tersebut. Bisa juga dimiliki pihak ketiga atau gabungan antara
                                                                       organisasi dan pihak ketiga.</p>
   <br>
        <h2 align="center">MODEL SISTEM LAYANAN</h2>
        <br>
        <center> <img src="assets/img/sas.JPG" alt="Cloud Computing" style="height: 300px;"></center>
        <br>                                                        
        <p align="justify">Dalam komputasi awan yang didasari dengan sistem model layanan, di kategorikan
                                                                         kepada 3 jenis yaitu:</p>
   
        <p align="left">1. Infrastruktur sebagai layanan (IaaS)</p>
   
        <p align="justify">Infrastruktur sebagai layanan merupakan model layanan cloud yang memiliki
                                                                           dasar yang merupakan bentuk fisik server dan juga virtual server, kita tidak perlu
                                                                           membeli alat atau barang tambahan untuk IaaS, IaaS menyediakan resource cloud
                                                                           seperti server, storage, jaringan,dan juga ruang data center.</p>
   
        <p align="left">2. Platform sebagai layanan (PaaS)</p>
   
        <p align="justify">Platform sebagai layanan merupakan model layanan yang disediakan dalam
                                                                             bentuk platform dan bisa di manfaatkan oleh seluruh pengguna untuk membuat
                                                                             sebuah software atau pun aplikasi di atasnya, di PaaS kita mendapatkan
                                                                             framework yang bisa di develop, dan juga seluruh server, penyimpanan, dan
                                                                             jaringan dapat dikelola oleh perusahaan atau penyedia dari pihak ketiga.</p>
   
